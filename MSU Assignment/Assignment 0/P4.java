// a java program to show conversion of int to byte , double to int and double to byte

public class P4 {
    public static void main(String[] args) {
        int a=15;
        System.out.println("Integer value: "+a);
        System.out.println("Integer to Byte value: "+(byte)a);

        double b=5.6;
        System.out.println("Double value: "+b);
        System.out.println("Double to Integer value: "+(int)b);
        System.out.println("Double to Byte value: "+(byte)b);
    }
}