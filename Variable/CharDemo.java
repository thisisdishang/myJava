public class CharDemo {
    public static void main(String[] args) {
        char c1,c2;
        c1=89;
        c2='Y';
        System.out.println("Character 1 = "+c1+" Character 2 = "+c2);
        c1++;
        System.out.println("Character 1 After Increment = "+c1);
        c2--;
        System.out.println("Character 2 After Decrement = "+c2);
        }    
}